# # Задача 4: Петя, Катя и Сережа делают из бумаги журавликов.
s = int(input("Введите количество журавликов: "))
p_s = s // 3  # количество журавликов, сделанных каждым из Пети и Сережи
k = p_s // 2  # количество журавликов, сделанных Катей

print("Петя:", p_s)
print("Катя:", k)
print("Сережа:", p_s)